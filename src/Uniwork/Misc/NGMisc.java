package Uniwork.Misc;

public final class NGMisc {

    public static Double Min(Double aA, Double aB) {
        if (aA <= aB)
            return aA;
        else
            return aB;
    }

    public static Double Max(Double aA, Double aB) {
        if (aA >= aB)
            return aA;
        else
            return aB;
    }

}
