package Uniwork.Graphics;

public class Point2D {

    protected Integer FX;
    protected Integer FY;

    public void setX(Integer aValue) {
        FX = aValue;
    }

    public Integer getX() {
        return FX;
    }

    public void setY(Integer aValue) {
        FY = aValue;
    }

    public Integer getY() {
        return FY;
    }

}
